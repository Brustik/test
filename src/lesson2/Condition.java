package lesson2;

public class Condition {
    public static void main(String[] args) {
        int x = 10;
        int y = 15;

        if (x > y) {
            System.out.println("X больше Y");
        } else if (x == y) {
            System.out.println("X равен Y");
        }
        else {
            System.out.println("X не больше Y");
        }







    }




}
